    public void setStack() throws IOException
    {
        String book,author;
         File fr =new File(us.Path);
         BufferedReader buffer;
           buffer = new BufferedReader(new FileReader(fr));
          String line=buffer.readLine();
            
                while(line!=null)
                {
                    if(line.equals("*****"))
                    {
                        break;
                    }
                    book=line;
                    line=buffer.readLine();
                    author=line;
                    line=buffer.readLine();
                    st.insertFirst(book, author);
                  
                }
                buffer.close();
    }
    
