      public void Fill2()
    {
        DefaultListModel add=new DefaultListModel();  
          
        if (st.isEmpty())
        {
                NoItem.setVisible(true);
                Pop.setVisible(false);
             
                  int countNo=1;
              
                  jList1.setVisible(false);
                  Clear.setVisible(false);
                  Selected.setVisible(false);
              
               
        }
        
        else
        {
            navigate=st.set();
        
               int countNo=1;
               int StringNo=0;
               
              
              
                while(navigate!=null)
                {
                add.addElement(countNo+". "+navigate.name+" - by - "+ navigate.author);
                jList1.setModel(add);
                BookNames[StringNo]=navigate.name;
                navigate=st.moveForward(navigate);
             
                countNo=countNo+1;
                StringNo=StringNo+1;
                }
        }
                   
            }
      
      public void delete(String name) throws FileNotFoundException, IOException
      {
         StringBuilder sb = new StringBuilder();
    File f =new File(us.Path);
    try (Scanner sc = new Scanner(f)) {
        String currentLine;
        while(sc.hasNext()){
            currentLine = sc.nextLine();
            if(currentLine.equals(name)){
               currentLine = sc.nextLine();
                continue;
//skips lineToRemove
            }
            sb.append(currentLine).append("\n");
        }
        
    }
   
    
    //Delete File Content
    PrintWriter pw = new PrintWriter(f);
    pw.close();

    BufferedWriter writer = new BufferedWriter(new FileWriter(f, true));
    writer.append(sb.toString());
    writer.close();
      }
           
        
       
    

