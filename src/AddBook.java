    public void add(String name, String author, String year, String category, String rack, String rackn) throws IOException
    {
         StringBuilder sb = new StringBuilder();
         File f=new File("E:\\3rd Semester\\DSA Lab\\Final_Project\\src\\Data.txt");
       try (Scanner sc = new Scanner(f)) {
        String currentLine;
        while(sc.hasNext()){
            currentLine = sc.nextLine();
            if(currentLine.equals("*****")){
              
                break;
//skips lineToRemove
            }
            sb.append(currentLine).append("\n");
        }
    }
    //Delete File Content
    PrintWriter pw = new PrintWriter(f);
    pw.close();

    BufferedWriter writer = new BufferedWriter(new FileWriter(f, true));
    writer.append(sb.toString());
    writer.close();
    
     WriteNow(name,author,year,category,rack,rackn);
    }
    
   public void  WriteNow(String name, String author, String year, String category, String rack, String rackn) throws IOException
   {
       File f=new File("E:\\3rd Semester\\DSA Lab\\Final_Project\\src\\Data.txt");
     PrintWriter wr =new PrintWriter(new FileWriter(f, true));
       BufferedWriter bw =new BufferedWriter(wr);      
            
    
     bw.write(name);
     bw.newLine();
     bw.write("///");
     bw.newLine();
     bw.write(author);
     bw.newLine();
     bw.write(year);
     bw.newLine();
     bw.write(category);
     bw.newLine();
     bw.write(rackn+rack);
     bw.newLine();
     bw.write("*****");
     bw.close();
   }

