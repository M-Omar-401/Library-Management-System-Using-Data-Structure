    private void next1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_next1ActionPerformed
        // TODO add your handling code here:
        
      
        String category, rack, name, author, year;
        String rackn="";
        category= jComboBox1.getSelectedItem().toString();
        rack= jComboBox2.getSelectedItem().toString();
        name = this.NameField.getText();
        author = this.AuthorField.getText();
        
       
        year = this.YearField.getText();
        
        
        switch(category)
        {
            case "Fiction" : rackn="F"; break;
            
            case "Political" : rackn="P"; break;
            
            case "Science" : rackn="S"; break;
            
            case "Biography" : rackn="B"; break;
            
            case "Islamic" : rackn="I"; break;
            
            case "Magazines" : rackn="M"; break;
        }
        
        try {
            add(name, author, year, category, rack, rackn);
        } catch (IOException ex) {
            Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        DialogAdd.setLocationRelativeTo(this);

        DialogAdd.setVisible(true);
        
    }//GEN-LAST:event_next1ActionPerformed

